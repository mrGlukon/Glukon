#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	k[13];
	int		i;

	i = 11;
	if (nb == -2147483648)
	{
		*(k + 1) = 2 + '0';
		nb = -147483648;
	}
	if (nb == 0)
		*(k + 0) = '0' + 0;
	if (nb < 0)
	{
		*(k + 0) = '-';
		nb = nb * (-1);
	}
	while (nb > 0)
	{
		*(k + i) = (nb % 10) + '0';
		i--;
		nb = nb / 10;
	}
	write(1, k, 12);
	write(1, "\n", 1);
}
